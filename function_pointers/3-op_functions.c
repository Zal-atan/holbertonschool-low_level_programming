#include "3-calc.h"

/**
 * op_add - returns sum of a and b
 *
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns a minus b
 *
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns a * b
 *
 * @a: first number input
 * @b: second input number
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns a divided by b
 *
 * @a: first input number
 * @b: second input number
 * Return: returns a/b
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - returns a % b
 *
 * @a: first input number
 * @b: second input number
 * Return: returns a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
